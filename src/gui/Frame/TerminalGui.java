    private void consolaTextAreaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_consolaTextAreaKeyTyped
        if (filaDeCursor != filaDeInstruccion || evt.getKeyChar() == '@') {
            evt.consume();
        }
    }//GEN-LAST:event_consolaTextAreaKeyTyped

